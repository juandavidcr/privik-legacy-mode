				<a name="r1"><h3>Respuesta</h3></a>
				<p>La movilidad es la posibilidad de cursar un periodo de estudios en una institución nacional o extranjera, con la cual exista un convenio establecido. En el caso de la Unidad Cuajimalpa, el periodo de movilidad está contemplado en todos los planes de estudio, por lo cual es obligatorio realizarlo.
				La movilidad implica tu incorporación como estudiante en otra institución, sin perder tus derechos como alumno de la UAM-Cuajimalpa. Esto es con el objetivo de brindarte la seguridad de que, una vez cursados los créditos académicos en otra institución, serán reconocidos en tu institución de origen a tu regreso.
				El periodo máximo de movilidad que puedes cursar en otra institución es de dos trimestres.
				</p>	
