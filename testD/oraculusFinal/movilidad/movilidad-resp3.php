				<h3 class="caption text-center">Documentación o Imágenes</h3>

		Para la beca de la unidad Cuajimalpa para movilidad internacional:

			Estar inscrito(a) al año escolar y haber estado inscrito(a) a unidades de enseñanza-aprendizaje (UEA) o módulo en el trimestre inmediato anterior a la fecha de presentación de la solicitud de beca.
			Tener un promedio mínimo de 8.5 (ocho punto cinco), con excepción de las Divisiones de Ciencias Básicas e Ingeniería y de Ciencias Naturales e Ingeniería, que será de 8.0 (ocho).
			Haber cubierto al menos el 50% de los créditos del plan de estudios al que se encuentre inscrito(a).
			Cursar el número de materias y créditos equivalente a una carga de tiempo completo de licenciatura en la institución receptora.
			 No haber recibido en trimestres anteriores, ni durante el tiempo de disfrute de esta beca, otra beca o apoyo económico que persiga los mismos propósitos.
			 Contar con póliza de seguro de gastos médicos internacional, válido durante su estancia en el extranjero. En caso de no presentar copia de ésta antes de su partida, no se podrá realizar el pago correspondiente.
			 Firmar la carta compromiso, una vez que se obtenga la beca.
			Documentos a presentar:
			Carta de aceptación o carta expedida por el área de apoyo académico correspondiente en la que se indique que el (la) solicitante fue postulado(a) y está en trámite su aceptación por la institución receptora.
			Copia de la solicitud presentada a la institución receptora.
			Constancia del idioma requerido por la institución receptora, en su caso.
			Solicitud UAM de participante en programas de movilidad
			Copia del pasaporte con una vigencia mínima de un año a partir del inicio de la estancia de estudios. <a href="http://www.becas.uam.mx/11/05022014-Soli-Beca-Lic-Movilidad-Internacional.pdf">Documento</a></a>
